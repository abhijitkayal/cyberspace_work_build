import mongoose, { Schema, models, model } from "mongoose"

const orderSchema = new Schema(
  {
    planName: {
      type: String,
      required: true,
    },

    name: {
      type: String,
      required: true,
    },

    email: {
      type: String,
      required: true,
    },

    phone: {
      type: String,
      required: true,
    },

    address: {
      type: String,
      required: true,
    },

    businessName: {
      type: String,
      required: true,
    },

    description: {
      type: String,
      required: true,
    },
  },
  {
    timestamps: true,
  }
)

export const Order =
  models.Order || model("Order", orderSchema)